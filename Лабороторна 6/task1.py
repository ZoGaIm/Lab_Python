lst = list(map(int, input("Введіть список: ").split()))

print(f"Оригінальний список: {lst}")

if len(lst) < 2:
    print("Список має менше двох елементів — видалення неможливе.")
else:
    del lst[1]

    if len(lst) >= 2:
        del lst[-2]

    print(f"Список після видалення другого і передостаннього елементів: {lst}")
